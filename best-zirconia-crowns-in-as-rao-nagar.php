                            </p>

                            <!-- Two Column -->
                            <p><img src="img/services/tooth-caps.jpg" alt="" class="img-fluid"></p>
                                <h3 class="mb-4">1. Metal Ceramic Crowns:</h3>
                                <p>Composition: Metal ceramic crowns are constructed with a metal alloy base covered by
                                    layers of tooth-colored ceramic.</p>
                                <h3 class="mb-4">Advantages:</strong></h3>
                                <ul>
                                    <li><strong>Strength:</strong>&nbsp;Metal ceramic crowns are known for their
                                        exceptional strength and durability, making them ideal for molars and other
                                        teeth that withstand significant chewing forces.</li>
                                    <li><strong>Longevity:</strong>&nbsp;These crowns have a proven track record of
                                        longevity, often lasting for many years with proper care.</li>
                                    <li><strong>Biocompatibility:</strong>&nbsp;The metal used is typically
                                        biocompatible, and the ceramic layers provide a natural-looking
                                        appearance.<br><br></li>
                                </ul>
                                <p><strong>Applications:</strong>&nbsp;Metal ceramic crowns are commonly used for
                                    posterior (back) teeth restorations where strength and durability are paramount.
                                    However, their metallic appearance may not be suitable for highly visible front
                                    teeth.</p>
                                <h3 class="mb-4">2. Direct Metal Laser Sintering (DMLS) Crowns:</h3>
                                <p><strong>Composition:</strong>&nbsp;DMLS crowns are made from a solid block of metal,
                                    typically a high-strength alloy, using a 3D printing technology called direct metal
                                    laser sintering.</p>
                                <h3 class="mb-4">Advantages:</h3>
                                <ul>
                                    <li><strong>Precision:</strong>&nbsp;DMLS technology allows for highly precise and
                                        customized crown designs, ensuring a perfect fit.</li>
                                    <li><strong>Strength:</strong>&nbsp;These crowns are exceptionally strong and
                                        resistant to wear, making them suitable for posterior teeth.</li>
                                    <li><strong>Minimal Tooth Removal:</strong>&nbsp;DMLS crowns require less tooth
                                        preparation, preserving more of the natural tooth structure.<br><br></li>
                                </ul>
                                <p><strong>Applications:</strong>&nbsp;DMLS crowns are ideal for cases where precision,
                                    strength, and minimal tooth reduction are crucial. They are often used for molars
                                    and premolars.</p>
                                <h3 class="mb-4">3. Zirconia Crowns:</h3>
                                <p>Composition: Zirconia crowns are made from zirconium dioxide, a strong and
                                    tooth-colored ceramic material.</p>
                                <p><strong>Advantages:</strong></p>
                                <ul>
                                    <li><strong>Strength:</strong>&nbsp;Zirconia crowns are renowned for their
                                        exceptional strength, comparable to metal crowns, making them suitable for
                                        posterior teeth.</li>
                                    <li><strong>Aesthetics:</strong>&nbsp;Zirconia crowns offer excellent aesthetics, as
                                        they closely resemble the natural color and translucency of teeth.</li>
                                    <li><strong>Biocompatibility:</strong>&nbsp;Zirconia is biocompatible, making it
                                        suitable for patients with metal allergies or sensitivities.<br><br></li>
                                </ul>
                                <p><strong>Applications:</strong>&nbsp;Zirconia crowns are versatile and can be used for
                                    both posterior and anterior (front) teeth restorations. They are particularly
                                    well-suited for cases where both strength and aesthetics are important.</p>
                                <h3 class="mb-4">Considerations for Crown Selection:</h3>
                                <p>The choice of crown material depends on several factors, including the location of
