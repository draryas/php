                                  <li><strong>Myth: Root Canal Treatment is Painful</strong><!-- wp:list -->
                                      <ul>
                                          <!-- wp:list-item -->
                                          <li>Fact: With today's advanced anesthetics and technology, a root canal is as
                                              painless as having a dental filling.</li>
