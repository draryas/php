                          <h2>What To Expect During A Dental Crown Procedure?</h2>
                          <p><img src="<?php echo $url; ?>img/blog/17.jpg" class="img-fluid"></p>
                          <div class="">
                              <!-- wp:paragraph -->
                              <p>Dental crowns are tooth-shaped caps that are placed over damaged or decayed teeth to
                                  restore their shape, size, and strength. If your dentist recommends a dental crown, it
                                  is important to understand what to expect during the procedure.</p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Numbing the Area with a Local Anesthetic</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>Before the procedure begins, your dentist will numb the area around the tooth with
                                      a local anesthetic. This will help ensure that you're comfortable throughout the
                                      procedure.</li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Removing Any Decay or Damage from the Tooth</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>Once the area is numb, your dentist will use a dental drill to remove any decay or
                                      damage from the tooth. This is necessary to create a stable base for the crown.
                                  </li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Shaping the Tooth to Prepare it for the Crown</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>After the tooth has been cleaned and prepared, your dentist will shape it to
                                      prepare it for the crown. This may involve removing additional material from the
                                      tooth to ensure that the crown fits properly.</li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Taking Impressions of the Tooth to Create a Custom-Made Crown</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>Next, your dentist will take impressions of the tooth to create a custom-made
                                      crown. These impressions will be used to create a mold of the tooth, which will
                                      then be sent to a dental laboratory where the crown will be made.</li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Placing a Temporary Crown Over the Tooth While the Permanent Crown is Being
                                      Made</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>To protect the prepared tooth while the permanent crown is being made, your
                                      dentist will place a temporary crown over the tooth. This will help prevent any
                                      further damage to the tooth and allow you to eat and speak normally while you wait
                                      for the permanent crown.</li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Removing the Temporary Crown and Cementing the Permanent Crown in
                                      Place</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:list -->
                              <ul>
                                  <!-- wp:list-item -->
                                  <li>Once the permanent crown has been made, your dentist will remove the temporary
                                      crown and cement the permanent crown in place. Your dentist will ensure that the
                                      crown fits properly and matches the color of your surrounding teeth.</li>
                                  <!-- /wp:list-item -->
                              </ul>
                              <!-- /wp:list -->

                              <!-- wp:paragraph -->
                              <p></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p><strong>Conclusion</strong></p>
                              <!-- /wp:paragraph -->

                              <!-- wp:paragraph -->
                              <p>Dental crowns are a common dental procedure that can restore the function and
                                  appearance of damaged or decayed teeth. While the procedure may seem daunting,
                                  understanding what to expect can help ease any anxiety you may have. With proper care,
                                  dental crowns can last for many years, providing you with a healthy and beautiful
                                  smile.</p>
